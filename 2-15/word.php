<br>VSP：1つの仮想サーバー群を個別で利用<br>
　Visual Private Serverの略。サーバーに複数の仮想サーバーを構築し、その一つをユーザーに貸し出す。仮想サーバーとは、もともと一つのものを分割し、仮想的に複数のサーバーがあるような状態にしている。<br>
