<br>クラウドサーバー、レンタルサーバー、VPSの違い<br>
レンタルサーバー：1つの物理サーバーを複数のユーザーで共有<br>
　OS・データベース・httpサーバーをインストールした物理サーバーを、利用者で共有するもの。本来、webサイトやECサイトを構築・運営するには、物理サーバーや回線を用意する必要があるが、これには高度な知識・費用が必要。レンタルサーバーでは、それらの設備の管理をベンダーに一任したうえで運営できる。<br>
